// Current language (defaults to saved or browser preference)
(function(){
  if (typeof window._localeInit === 'undefined') {
    window._localeInit = true;
    const saved = localStorage.getItem('foodie-lang');
    if (saved) {
      window.CURRENT_LANG = saved;
    } else if (navigator.language && navigator.language.startsWith('ru')) {
      window.CURRENT_LANG = 'ru';
    } else {
      window.CURRENT_LANG = 'en';
    }
  }
})();

function t(key) {
  var locale = window.LOCALES && window.LOCALES[window.CURRENT_LANG];
  return (locale && locale[key]) || (window.LOCALES && window.LOCALES['en'] && window.LOCALES['en'][key]) || key;
}

function loc(fieldEn, fieldRu) {
  if (window.CURRENT_LANG === 'ru' && fieldRu !== undefined && fieldRu !== '') return fieldRu;
  return fieldEn;
}
